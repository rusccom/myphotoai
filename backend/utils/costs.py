# --- Загрузка и получение стоимости ---

@lru_cache() # Кэшируем результат, чтобы не читать файл каждый раз
def load_costs():
    """Загружает конфигурацию цен из costs_config.json."""
    config_path = os.path.join(os.path.dirname(__file__), '..', 'costs_config.json')
    try:
        with open(config_path, 'r') as f:
            costs = json.load(f)
            logging.info("Costs configuration loaded successfully.")
            return costs
    except FileNotFoundError:
        logging.error(f"Costs configuration file not found at {config_path}")
        return {}
    except json.JSONDecodeError:
        logging.error(f"Error decoding JSON from {config_path}")
        return {}
    except Exception as e:
        logging.exception(f"An unexpected error occurred while loading costs config: {e}")
        return {}

def get_action_cost(action_type: str) -> int | None:
    """Возвращает стоимость для указанного типа действия."""
    costs = load_costs()
    cost = costs.get(action_type)
    if cost is None:
        logging.warning(f"Cost for action type '{action_type}' not found in configuration.")
    elif not isinstance(cost, int) or cost < 0:
        logging.error(f"Invalid cost value '{cost}' for action type '{action_type}' found in configuration. Must be a non-negative integer.")
        return None # Возвращаем None, если значение некорректно
    return cost

# --- Получение баланса и списание ---

def get_user_balance(user_id: int) -> int | None:
    """Получает текущий баланс пользователя из представления user_balance."""
    balance = 0 # Возвращаем 0 по умолчанию или при ошибке
    try:
        # Убедимся, что представление user_balance существует и работает
        sql = text("SELECT balance_points FROM user_balance WHERE user_id = :user_id")
        result = db.session.execute(sql, {'user_id': user_id}).fetchone()
        if result and result[0] is not None:
            balance = int(result[0])
            logging.debug(f"Fetched balance for user {user_id}: {balance}")
            return balance
        else:
             # Если в представлении нет записи (новый пользователь?), баланс 0
             logging.warning(f"No balance record found for user {user_id} in user_balance view. Assuming 0.")
             return 0
    except Exception as e:
        # Логируем конкретную ошибку (например, если представления нет)
        logging.exception(f"Failed to fetch balance for user {user_id} from user_balance view: {e}")
        # В случае ошибки неясно, какой баланс правильный.
        # Можно вернуть None, чтобы сигнализировать об ошибке вызывающей функции.
        # Или вернуть 0, чтобы предотвратить выполнение действия. Выберем None.
        return None


def check_balance_and_deduct(user_id: int, action_type: str, quantity: int = 1) -> tuple[bool, str | None, int | None]:
    """
    Проверяет баланс пользователя и списывает стоимость действия.
    Создает запись в PaidAction при успехе.

    Args:
        user_id: ID пользователя.
        action_type: Тип действия из costs_config.json.
        quantity: Количество выполняемых действий (по умолчанию 1).

    Возвращает:
        tuple[bool, str | None, int | None]: 
            (True, None, new_balance) при успехе,
            (False, error_message, current_balance) при недостатке средств,
            (False, error_message, None) при других ошибках.
    """
    if quantity <= 0:
         logging.warning(f"Attempted to perform action '{action_type}' with invalid quantity {quantity} for user {user_id}. Skipping.")
         # Возвращаем None для баланса при логической ошибке
         return False, "Quantity must be positive.", None

    cost_per_item = get_action_cost(action_type)
    if cost_per_item is None:
        logging.error(f"Cannot perform action '{action_type}' for user {user_id}: Cost is not defined or invalid.")
        # Возвращаем None для баланса при ошибке конфигурации
        return False, f"Cost for '{action_type}' is not configured correctly.", None

    total_cost = cost_per_item * quantity

    # Обработка случая, когда действие бесплатное
    if total_cost == 0:
        logging.info(f"Action '{action_type}' (x{quantity}) for user {user_id} has zero total cost. Skipping balance check and deduction.")
        # Если бесплатно, баланс не меняется, но надо его получить и вернуть
        current_balance = get_user_balance(user_id)
        return True, None, current_balance

    current_balance = get_user_balance(user_id)

    if current_balance is None:
        # Ошибка получения баланса
        return False, "Failed to retrieve user balance. Please try again later.", None

    if current_balance < total_cost:
        logging.warning(f"Insufficient balance for user {user_id} to perform action '{action_type}' (x{quantity}). Required: {total_cost}, Available: {current_balance}")
        # Возвращаем текущий баланс при недостатке средств
        error_msg = f"Insufficient points. Required: {total_cost} ({cost_per_item} per item), Available: {current_balance}."
        return False, error_msg, current_balance

    # Списание средств
    try:
        # Создаем одну запись PaidAction на общую сумму
        paid_action = PaidAction(
            user_id=user_id,
            action_type=f"{action_type}_x{quantity}", # Добавляем количество к типу действия
            cost_points=total_cost
        )
        db.session.add(paid_action)
        db.session.commit()
        logging.info(f"Successfully deducted {total_cost} points from user {user_id} for action '{action_type}' (x{quantity}).")
        
        # Получаем НОВЫЙ баланс ПОСЛЕ списания
        new_balance = get_user_balance(user_id)
        if new_balance is not None:
            logging.info(f"New balance for user {user_id}: {new_balance}")
            return True, None, new_balance
        else:
            # Если не удалось получить новый баланс ПОСЛЕ списания - это проблема
            logging.error(f"CRITICAL: Failed to fetch balance for user {user_id} immediately after deduction!")
            # Возвращаем успех операции, но без нового баланса (фронтенд не сможет обновить)
            return True, None, None 

    except Exception as e:
        db.session.rollback()
        logging.exception(f"Failed to record paid action or deduct points for user {user_id}, action '{action_type}' (x{quantity}). Cost: {total_cost}")
        # Возвращаем None для баланса при ошибке транзакции
        return False, "Failed to record payment transaction. Please try again.", None


